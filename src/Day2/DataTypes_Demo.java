package Day2;

public class DataTypes_Demo
{
    public static void main(String args[])
    {
        //byte data type
        byte by=100;
        System.out.println("by");

        //short data type
        short sh=1000;
        System.out.println(sh);

        //int data type
        int a=14000;
        System.out.println(a);

        //long data type     need to add L
        long L=100000000000000L;
        System.out.println(L);

        //float data type  need to add F
        float prise=33.4F;
        System.out.println(prise);

        //double data type
        double cost=111.908;
        System.out.println(cost);

        //string data type
        String Sh="Kumar";
        System.out.println(Sh);

        //Char data type
        char Grade='A';
        System.out.println(Grade);

        //boolean data type
        boolean boo=true;
        System.out.println(boo);





    }
}
